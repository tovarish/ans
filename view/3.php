<p>Для загальної задачі лінійного програмування використовуються такі поняття.</p>
<p>Вектор <sub><img src="/img/image151.gif"></sub>, координати якого задовольняють систему обмежень (3.2) та умови невід’ємності змінних (3.3), називається <b><i>допустимим розв’язком (планом) задачі лінійного програмування .</i></b></p>
<p>Допустимий план <sub><img src="/img/image151.gif"></sub> називається <b><i>опорним планом</i> </b>задачі лінійного програмування, якщо він задовольняє не менше, ніж <sub><img src="/img/image153.gif"></sub> лінійно незалежних обмежень системи (3.2) у вигляді рівностей, а також обмеження (3.3) щодо невід’ємності змінних.</p>
<p>Опорний план <sub><img src="/img/image151.gif"></sub>, називається <b><i>невиродженим</i></b>, якщо він містить точно <sub><img src="/img/image153.gif"></sub> додатних змінних, інакше він <b><i>вироджений</i></b>.</p>
<p>Опорний план <sub><img src="/img/image156.gif"></sub>, за якого цільова функція (3.1) досягає максимального (чи мінімального) значення, називається <b><i>оптимальним розв’язком (планом) задачі лінійного програмування.</i></b></p>
