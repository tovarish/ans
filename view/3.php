<p>Цю функцію називають <b><i>цільовою функцією</i></b>, вона моделює поставлену в задачі мету.</p>
<p>У задачах лінійного програмування можна виділити два типи однорідних обмежень на вибір змінних: у вигляді лінійних нерівностей або у вигляді лінійних рівнянь. Крім того, для спрощення можна формулювати задачу лише для мінімуму цільової функції. Якщо ж в конкретній задачі треба визначити максимум функції <sub><img src="/img/image139.gif"></sub>, то це те саме, що шукати мінімум функції <sub><img src="/img/image141.gif"></sub>.</p>
<p>З цього випливають три форми задач лінійного програмування:</p>
<p>1) загальна задача лінійного програмування;</p>
<p>2) стандартна ( симетрична);</p>
<p>3) канонічна.</p>
